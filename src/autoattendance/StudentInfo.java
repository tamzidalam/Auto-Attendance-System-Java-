    private void addStudentBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addStudentBtnActionPerformed

       int cid = 0;

        DatabaseConnection dbConnection = new DatabaseConnection();

        String studentId = IDTextField.getText();
        String firstName = firstNameTextField.getText();
        String lastName = lastNameTextField.getText();
        String courseName = studentInfoCourseComboBox1.getSelectedItem().toString();
        String section = studentInfoSectionComboBox1.getSelectedItem().toString();

        try {
            Connection con = dbConnection.createConnection();
            Statement st = con.createStatement();
            ResultSet rs2 = st.executeQuery("SELECT * FROM `students` where `students`.`Student_Id` = '" + studentId + "'");
            if (!rs2.next()) {
                Statement st1 = con.createStatement();
                st1.executeUpdate("INSERT INTO `students` (`Student_Id`, `First_Name`, `Last_Name`)  VALUES ('" + studentId + "','" + lastName + "','" + firstName + "');");
                ResultSet rs1 = st1.executeQuery("SELECT * FROM `course` where `course`.`Course_Name` = \'" + courseName + "\' and `course`.`Section` = \'" + section + "\' ");

                if (rs1.next()) {
                    cid = rs1.getInt("id");
                }

                Statement st2 = con.createStatement();
                st2.executeUpdate("INSERT INTO `studentcourse` (`CID`, `SID`)  VALUES ('" + cid + "','" + studentId + "');");
                JOptionPane.showMessageDialog(null, "Student's information added");
            } else {

                ResultSet rs1 = st.executeQuery("SELECT * FROM `course` where `course`.`Course_Name` = \'" + courseName + "\' and `course`.`Section` = \'" + section + "\' ");

                if (rs1.next()) {
                    cid = rs1.getInt("id");
                }
                Statement st2 = con.createStatement();
                ResultSet rs3 = st2.executeQuery("SELECT * FROM `studentcourse` where `studentcourse`.`SID` ='" + studentId + "' and `studentcourse`.`CID` = " + cid + " ");
                if (rs3.next()) {
                    JOptionPane.showMessageDialog(null, " Cannnot add.Student is already enlisted on that course");
                } else {
                    Statement st3 = con.createStatement();
                    st3.executeUpdate("INSERT INTO `studentcourse` (`CID`, `SID`)  VALUES ('" + cid + "','" + studentId + "');");
                    JOptionPane.showMessageDialog(null, "Student's information added");
                }
            }
    
        } catch (Exception ex) {
            System.out.println(ex);
            ex.printStackTrace();
        }

        loadData();
    }//GEN-LAST:event_addStudentBtnActionPerformed

