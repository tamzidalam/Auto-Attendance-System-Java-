      void loadData() {

        try {

            DatabaseConnection dbConnection = new DatabaseConnection();
            Connection con = dbConnection.createConnection();

            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select `students`.`Student_Id`,`students`.`Last_Name`,`students`.`First_Name`,`course`.`Course_Name`,`course`.`Section` from `students` ,`course`,`studentcourse` Where `students`.`Student_Id`=`studentcourse`.`SID` and `course`.`id`=`studentcourse`.`CID`");
            
            
            while (stdInfoTable.getRowCount() > 0) {

                ((DefaultTableModel) stdInfoTable.getModel()).removeRow(0);

            }

            int col = rs.getMetaData().getColumnCount();
            while (rs.next()) {
                Object[] rows = new Object[col];
                for (int i = 1; i <= col; i++) {
                    rows[i - 1] = rs.getObject(i);
                }
                ((DefaultTableModel) stdInfoTable.getModel()).insertRow(rs.getRow() - 1, rows);

            }
            rs.close();
            st.close();

        } catch (Exception ex) {

        }
    }

